//Enhanced For Loop with Arrays
public class ex15 {
    public static void main(String[] args) {
         int jason[] = {4,54,54,54,54,+64,987,64,64,8978,56};
         for (int element : jason) {
             System.out.print("\n" + element);
             System.out.print("\n" + element*element);
         }
    }
}
